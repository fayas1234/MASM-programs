ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	MSG1 DB "ELEMENT FOUND $"
	ARRAY DB 20H,26H,56H,02H,03H,0AH
	MSG2 DB "ELEMENT NOT FOUND $"
DATA ENDS

PRINT MACRO MSG
	LEA DX,MSG
	
	MOV AH,09H
	INT 21H
ENDM

STACK SEGMENT 

STACK ENDS

CODE SEGMENT
	START:
		MOV AX,DATA
		MOV DS,AX
		
		MOV CL,06H
		
		MOV AL,0AH
		LEA SI,ARRAY
		
		LP:
			MOV BL,[SI]
			
			CMP AL,BL
			JNE L1
			
			PRINT MSG1
			MOV AH,4CH
			INT 21H
			
		L1:
			INC SI
			DEC CL
			CMP CL,00H
			JNE LP
			
			PRINT MSG2
			MOV AH,4CH
			INT 21H
CODE ENDS
END START
			
			
