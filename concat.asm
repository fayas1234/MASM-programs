;PROGRAM TO PERFORM STRING CONATENATION

ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	STR1 DB "ABCD $"
	STR2 DB "BCDF $"
DATA ENDS


CODE SEGMENT
	START:
		MOV AX,DATA
		MOV DS,AX
		
		LEA SI,STR1
		LEA DI,STR2
		
		MOV AL,'$'
		
	LP:
		CMP AL,[SI]
		JZ NEXT
		
		INC SI
		
		JMP LP
		
	
	NEXT:
		CMP AL,[DI]
		JZ EXIT
		
		MOV AH,[DI]
		MOV [SI],AH
		
		INC DI
		INC SI
		JMP NEXT
		
	EXIT:
		LEA DX,STR1
		MOV AH,09H
		INT 21H
		
		MOV AH,4CH 
		INT 21H
		
CODE ENDS
END START
