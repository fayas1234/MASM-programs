;Program to impelement the searching of 16-bit numbers

ASSUME CS:CODE,DS:DATA

DATA SEGMENT 
	ARR DW 1234H,2020H,2025H,0A0FH,0003H
	MSG1 DB "ELEMENT FOUND $"
	MSG2 DB "ELEMENT NOT FOUND $"
DATA ENDS

STACK SEGMENT
	
STACK ENDS

CODE SEGMENT

START:
	MOV AX,DATA
	MOV DS,AX
	
	LEA SI,ARR
	
	MOV AX,1234H
	MOV CL,05H
	
	LP:
		CMP AX,[SI]
		JZ FO
		
		ADD SI,02H
		DEC CL
		JNZ LP
		
		JMP FN
		
	FO:
		LEA DX,MSG1
		
		MOV AH,09H
		INT 21H
		
	FN:
		LEA DX,MSG2
		
		MOV AH,09H
		INT 21H
		
	MOV AH,4CH
	INT 21H
	
CODE ENDS
END START
